#ifndef __OLED_H
#define __OLED_H

#include "main.h"

/* SSD1306 I2C 地址 (7位: 0x3C, 通常 DC 引脚接地) */
#define OLED_ADDR     0x78  /* 8位写入地址 = 0x3C << 1 */
#define OLED_WIDTH    128
#define OLED_HEIGHT   64
#define OLED_PAGES    (OLED_HEIGHT / 8)

/* ------------------------------------------------------------------ */
/* 函数: OLED_Init                                                      */
/* 说明: 初始化 SSD1306 OLED 显示屏，配置显示参数，开机显示              */
/* 参数: hi2c - I2C 句柄指针 (如 &hi2c1)                               */
/* ------------------------------------------------------------------ */
void OLED_Init(I2C_HandleTypeDef *hi2c);

/* ------------------------------------------------------------------ */
/* 函数: OLED_Clear                                                     */
/* 说明: 清空整个屏幕 (所有像素熄灭)，缓冲区清零并刷新                   */
/* ------------------------------------------------------------------ */
void OLED_Clear(void);

/* ------------------------------------------------------------------ */
/* 函数: OLED_Display                                                   */
/* 说明: 将内部缓冲区的内容整体刷新到 OLED 屏幕                          */
/* ------------------------------------------------------------------ */
void OLED_Display(void);

/* ------------------------------------------------------------------ */
/* 函数: OLED_ShowString                                                */
/* 说明: 在指定位置显示字符串 (8x16 字体，自动换行)                      */
/* 参数: x   - 起始列坐标 (0~127, 8 的倍数对齐最佳)                     */
/*        y   - 起始行坐标 (0~63, 16 的倍数对齐最佳)                    */
/*        str - 要显示的字符串指针                                       */
/* ------------------------------------------------------------------ */
void OLED_ShowString(uint8_t x, uint8_t y, const char *str);

/* ------------------------------------------------------------------ */
/* 函数: OLED_ShowChar                                                  */
/* 说明: 在指定位置显示单个 ASCII 字符 (8x16 字体，占 2 页)             */
/* 参数: x   - 列坐标 (0~127)                                           */
/*        y   - 行坐标 (0~63, 16 的倍数对齐最佳)                        */
/*        ch  - ASCII 字符                                              */
/* ------------------------------------------------------------------ */
void OLED_ShowChar(uint8_t x, uint8_t y, char ch);

/* ------------------------------------------------------------------ */
/* 函数: OLED_ShowNum                                                   */
/* 说明: 在指定位置显示无符号整数                                        */
/* 参数: x    - 起始列坐标 (0~127)                                      */
/*        y    - 页坐标 (0~7)                                           */
/*        num  - 要显示的数字                                           */
/*        len  - 数字位数 (不足补前导零)                                 */
/* ------------------------------------------------------------------ */
void OLED_ShowNum(uint8_t x, uint8_t y, uint32_t num, uint8_t len);

/* ------------------------------------------------------------------ */
/* 函数: OLED_ShowSignedNum                                             */
/* 说明: 在指定位置显示有符号整数 (带负号)                                */
/* 参数: x    - 起始列坐标 (0~127)                                      */
/*        y    - 页坐标 (0~7)                                           */
/*        num  - 要显示的有符号数字                                     */
/*        len  - 数字位数 (不含负号)                                     */
/* ------------------------------------------------------------------ */
void OLED_ShowSignedNum(uint8_t x, uint8_t y, int32_t num, uint8_t len);

/* ------------------------------------------------------------------ */
/* 函数: OLED_ShowFloat                                                 */
/* 说明: 在指定位置显示浮点数                                            */
/* 参数: x      - 起始列坐标 (0~127)                                    */
/*        y      - 页坐标 (0~7)                                         */
/*        num    - 要显示的浮点数                                       */
/*        intLen - 整数部分位数                                         */
/*        decLen - 小数部分位数                                         */
/* ------------------------------------------------------------------ */
void OLED_ShowFloat(uint8_t x, uint8_t y, float num, uint8_t intLen, uint8_t decLen);

/* ------------------------------------------------------------------ */
/* 函数: OLED_ShowHex                                                   */
/* 说明: 在指定位置显示十六进制数 (带 "0x" 前缀)                         */
/* 参数: x    - 起始列坐标 (0~127)                                      */
/*        y    - 页坐标 (0~7)                                           */
/*        num  - 要显示的数字                                           */
/*        len  - 十六进制位数                                           */
/* ------------------------------------------------------------------ */
void OLED_ShowHex(uint8_t x, uint8_t y, uint32_t num, uint8_t len);

/* ------------------------------------------------------------------ */
/* 函数: OLED_ShowBin                                                   */
/* 说明: 在指定位置显示二进制数 (带 "0b" 前缀)                           */
/* 参数: x    - 起始列坐标 (0~127)                                      */
/*        y    - 页坐标 (0~7)                                           */
/*        num  - 要显示的数字                                           */
/*        len  - 二进制位数                                             */
/* ------------------------------------------------------------------ */
void OLED_ShowBin(uint8_t x, uint8_t y, uint32_t num, uint8_t len);

/* ------------------------------------------------------------------ */
/* 函数: OLED_DrawPoint                                                 */
/* 说明: 在指定位置画一个点 (仅修改缓冲区，调用 OLED_Display 后可见)      */
/* 参数: x - 列坐标 (0~127)                                             */
/*        y - 行坐标 (0~63)                                             */
/* ------------------------------------------------------------------ */
void OLED_DrawPoint(uint8_t x, uint8_t y);

/* ------------------------------------------------------------------ */
/* 函数: OLED_ClearPoint                                                */
/* 说明: 清除指定位置的点 (仅修改缓冲区，调用 OLED_Display 后可见)       */
/* 参数: x - 列坐标 (0~127)                                             */
/*        y - 行坐标 (0~63)                                             */
/* ------------------------------------------------------------------ */
void OLED_ClearPoint(uint8_t x, uint8_t y);

/* ------------------------------------------------------------------ */
/* 函数: OLED_DrawLine                                                  */
/* 说明: 使用 Bresenham 算法绘制直线 (仅修改缓冲区)                      */
/* 参数: x0, y0 - 起点坐标                                              */
/*        x1, y1 - 终点坐标                                             */
/* ------------------------------------------------------------------ */
void OLED_DrawLine(uint8_t x0, uint8_t y0, uint8_t x1, uint8_t y1);

/* ------------------------------------------------------------------ */
/* 函数: OLED_DrawRect                                                  */
/* 说明: 绘制矩形边框 (仅修改缓冲区)                                     */
/* 参数: x, y  - 左上角坐标                                             */
/*        w, h  - 宽度和高度                                            */
/* ------------------------------------------------------------------ */
void OLED_DrawRect(uint8_t x, uint8_t y, uint8_t w, uint8_t h);

/* ------------------------------------------------------------------ */
/* 函数: OLED_FillRect                                                  */
/* 说明: 绘制填充矩形 (仅修改缓冲区)                                     */
/* 参数: x, y  - 左上角坐标                                             */
/*        w, h  - 宽度和高度                                            */
/* ------------------------------------------------------------------ */
void OLED_FillRect(uint8_t x, uint8_t y, uint8_t w, uint8_t h);

/* ------------------------------------------------------------------ */
/* 函数: OLED_DrawCircle                                                */
/* 说明: 使用中点画圆算法绘制圆形边框 (仅修改缓冲区)                     */
/* 参数: cx, cy - 圆心坐标                                              */
/*        r     - 半径                                                  */
/* ------------------------------------------------------------------ */
void OLED_DrawCircle(uint8_t cx, uint8_t cy, uint8_t r);

/* ------------------------------------------------------------------ */
/* 函数: OLED_ShowChinese                                               */
/* 说明: 在指定位置显示一个 16x16 的中文字符                             */
/*       需要先在代码中定义中文字模数组                                   */
/* 参数: x - 列坐标 (0~112, 16 对齐更佳)                                */
/*        y - 页坐标 (0~6)                                              */
/*        n - 字模数组索引                                              */
/* ------------------------------------------------------------------ */
void OLED_ShowChinese(uint8_t x, uint8_t y, uint8_t n);

/* ------------------------------------------------------------------ */
/* 函数: OLED_Printf                                                    */
/* 说明: 在指定位置使用格式化字符串显示内容 (类似 printf)                 */
/*       支持 %d %u %x %b %f %c %s                                      */
/* 参数: x      - 起始列坐标 (0~127)                                    */
/*        y      - 页坐标 (0~7)                                         */
/*        fmt    - 格式化字符串                                          */
/*        ...    - 可变参数                                              */
/* ------------------------------------------------------------------ */
void OLED_Printf(uint8_t x, uint8_t y, const char *fmt, ...);

/* ------------------------------------------------------------------ */
/* 函数: OLED_SetContrast                                               */
/* 说明: 设置 OLED 对比度                                               */
/* 参数: contrast - 对比度值 (0~255, 默认 0x7F)                        */
/* ------------------------------------------------------------------ */
void OLED_SetContrast(uint8_t contrast);

#endif
